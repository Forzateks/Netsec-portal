-- v72 + v76: defense-in-depth for the engagements UPDATE flow.
-- Server-side stamps tracker_updated_at + updated_by on every update.
-- For non-manager updates, silently reverts every manager-only column to
-- its OLD value. Combined with the engagements_update RLS policy this
-- means an employee can edit only the fields they're supposed to —
-- even if they call the API directly.
CREATE OR REPLACE FUNCTION public.enforce_engagement_employee_edit_perms()
RETURNS trigger LANGUAGE plpgsql SECURITY DEFINER AS $$
BEGIN
  NEW.tracker_updated_at := now();
  NEW.updated_by         := current_employee_name();

  IF is_manager_user() THEN
    RETURN NEW;
  END IF;

  NEW.status               := OLD.status;
  NEW.owner_employee       := OLD.owner_employee;
  NEW.name                 := OLD.name;
  NEW.type                 := OLD.type;
  NEW.customer_id          := OLD.customer_id;
  NEW.country              := OLD.country;
  NEW.partner              := OLD.partner;
  NEW.start_date           := OLD.start_date;
  NEW.end_date             := OLD.end_date;
  NEW.license_expiry       := OLD.license_expiry;
  NEW.signed_off_on        := OLD.signed_off_on;
  NEW.project_order_no     := OLD.project_order_no;
  NEW.converted_to_project := OLD.converted_to_project;
  NEW.is_archived          := OLD.is_archived;
  NEW.archived_at          := OLD.archived_at;

  RETURN NEW;
END;
$$;

