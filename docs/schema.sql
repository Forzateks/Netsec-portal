-- ── 2. RLS HELPER FUNCTIONS ────────────────────────────────────────
-- Both are STABLE + SECURITY DEFINER so they can read user_profiles
-- without recursing into their own RLS policies. RLS policies on every
-- other table call into these — they must exist before the policies.

CREATE OR REPLACE FUNCTION public.current_employee_name() RETURNS text
LANGUAGE sql STABLE SECURITY DEFINER AS $$
  SELECT employee_name FROM public.user_profiles
  WHERE LOWER(email) = LOWER(auth.jwt()->>'email')
  LIMIT 1;
$$;

CREATE OR REPLACE FUNCTION public.is_manager_user() RETURNS boolean
LANGUAGE sql STABLE SECURITY DEFINER AS $$
  SELECT COALESCE(
    (SELECT is_manager FROM public.user_profiles
     WHERE LOWER(email) = LOWER(auth.jwt()->>'email') LIMIT 1),
    FALSE
  );
$$;

